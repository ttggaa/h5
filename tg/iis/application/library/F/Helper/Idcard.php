     * 判断是否为简体中文
     * 
     * @param string $str 要检测的字符串（必须为GBK编码）
     * @return bool
     */
    static public function isGB2312($str)
    {
    	$mb_len = mb_strlen($str, 'GBK');
    	$len = strlen($str);
    	if( ($mb_len * 2) != $len ) {
    		return false;
    	}
    	
    	for($i=0; $i<$len; $i++)
    	{
    		$ih = ord($str{$i});
    		++$i;
    		$il = ord($str{$i});
    		if( $ih >= 176 && $ih <= 247 && $il >= 161 && $il <= 254 ) {
    			continue;
    		}
    		if( $ih >= 129 && $ih <= 160 && $il >= 64 && $il <= 254 ) {
    			continue;
    		}
    		if( $ih >= 170 && $ih <= 254 && $il >= 64 && $il <= 160 ) {
    			continue;
    		}
    		return false;
    	}
    	
    	return true;
    }
    
