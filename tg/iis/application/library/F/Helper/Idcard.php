        if( ! preg_match('/^\d{17}([0-9X]{1})$/', $id) ) {
            return false;
        }
        $pro = array(11=>'北京',12=>'天津',13=>'河北',14=>'山西',15=>'内蒙古',21=>'辽宁',22=>'吉林',23=>'黑龙江',31=>'上海',32=>'江苏',33=>'浙江',34=>'安徽',35=>'福建',36=>'江西',37=>'山东',41=>'河南',42=>'湖北',43=>'湖南',44=>'广东',45=>'广西',46=>'海南',50=>'重庆',51=>'四川',52=>'贵州',53=>'云南',54=>'西藏 ',61=>'陕西',62=>'甘肃',63=>'青海',64=>'宁夏',65=>'新疆',71=>'台湾',81=>'香港',82=>'澳门',91=>'国外');
        if( ! array_key_exists(substr($id, 0, 2), $pro) ) {
            return false;
        }
        $y = intval(substr($id, 6, 4));
        $m = substr($id, 10, 2);
        $d = substr($id, 12, 2);
        $ty = intval(date('Y'));
        if( $y < ($ty - 70) || $y > ($ty - 5) || ! strtotime("{$y}-{$m}-{$d}") ) {
            return false;
        }
        
        $arr_int = array(7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2);
        $arr_ch = array('1', '0', 'X', '9', '8', '7', '6', '5', '4', '3', '2');
        $sign = 0;
        for ($i = 0; $i < 17; $i++)
        {
            $b = (int) $id{$i};
            $w = $arr_int[$i];
            $sign += $b * $w;
        }
        $n  = $sign % 11;
        $val_num = $arr_ch[$n];
        if( $val_num != substr($id, 17, 1) ) {
            return false;
        } else {
            return true;
        }
    }
